public class RegraDadosCodComments3 {

	String codigo_1 = "";
	String comentarios_1 = "";

	String codigo_2 = "";
	String comentarios_2 = "";

	public RegraDadosCodComments3() {
		super();
	}

	public RegraDadosCodComments3(String codigo_1, String comentarios_1, String codigo_2,
			String comentarios_2) {
		super();
		this.codigo_1 = codigo_1;
		this.comentarios_1 = comentarios_1;
		this.codigo_2 = codigo_2;
		this.comentarios_2 = comentarios_2;
	}

	public String getCodigo_1() {
		return codigo_1;
	}

	public void setCodigo_1(String codigo_1) {
		this.codigo_1 = codigo_1;
	}

	public String getComentarios_1() {
		return comentarios_1;
	}

	public void setComentarios_1(String comentarios_1) {
		this.comentarios_1 = comentarios_1;
	}

	public String getCodigo_2() {
		return codigo_2;
	}

	public void setCodigo_2(String codigo_2) {
		this.codigo_2 = codigo_2;
	}

	public String getComentarios_2() {
		return comentarios_2;
	}

	public void setComentarios_2(String comentarios_2) {
		this.comentarios_2 = comentarios_2;
	}

}
